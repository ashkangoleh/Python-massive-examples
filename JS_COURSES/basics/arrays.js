// ARRAYS
const arr = [1,2,3,"ashkan"]
// `push` increment at last index
arr.push(4)
// `unshift` increment at first index
arr.unshift(0)
// `pop` remove last index
arr.pop()
// `pop` remove first index
arr.shift()
// `in` statement loops through the (properties) of an Object:
// `of` statement loops through the (values) of an iterable object.
for(const item of arr){
  console.debug(item)
}


